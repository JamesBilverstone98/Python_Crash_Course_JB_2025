# SUMMING A MILLION = make a list of the numbers one-one million then use 
# the min, max, and sum functions on it

numbers = list(range(1, 10000001)) 
# loops through numbers one-one million and adds them to a list
print(min(numbers)) # prints the lowest number in the new list
print(max(numbers)) # prints out the highest number in the new list
print(sum(numbers)) # prints out the sum of all numbers in the new list